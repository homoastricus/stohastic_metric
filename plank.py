import numpy as np

l_p = 1.6*1e-35
def physical_sigma_r(r, N_total, l_p):
    """
    –§–∏–∑–∏—á–µ—Å–∫–∏ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è —à—É–º–∞
    N_total - –æ–±—â–µ–µ —á–∏—Å–ª–æ —è—á–µ–µ–∫ –≤ —Å–∏—Å—Ç–µ–º–µ
    """
    # 1. –õ–æ–∫–∞–ª—å–Ω—ã–π —à—É–º (–ø–ª–∞–Ω–∫–æ–≤—Å–∫–∏–π)
    sigma_local = np.sqrt(l_p) * np.exp(-r / 2.0)

    # 2. –ì–æ–ª–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–∏–π —à—É–º (–∑–∞–≤–∏—Å–∏—Ç –æ—Ç —Ä–∞–∑–º–µ—Ä–∞ —Å–∏—Å—Ç–µ–º—ã!)
    sigma_holo = np.sqrt(32.0 / N_total) * np.sqrt(l_p)

    # 3. –ö–≤–∞–¥—Ä–∞—Ç –ø–ª–∞–Ω–∫–æ–≤—Å–∫–æ–π –¥–ª–∏–Ω—ã
    sigma_planck = (l_p / (r + l_p)) ** 2

    # –ò–¢–û–ì–û–ì–û: —Å—É–º–º–∞ –≤—Å–µ—Ö –≤–∫–ª–∞–¥–æ–≤
    return sigma_local + sigma_holo + sigma_planck


# –¢–µ—Å—Ç–∏—Ä—É–µ–º –º–∞—Å—à—Ç–∞–±–∏—Ä–æ–≤–∞–Ω–∏–µ
sizes = {
    'Toy (650¬≥)': 2.75e8,
    '–ó–µ–º–ª—è': 1e20,
    '–ì–∞–ª–∞–∫—Ç–∏–∫–∞': 1e60,
    '–í—Å–µ–ª–µ–Ω–Ω–∞—è': 1e185
}

print("üî¨ –ú–ê–°–®–¢–ê–ë–ò–†–û–í–ê–ù–ò–ï –®–£–ú–ê:")
print(f"{'–°–∏—Å—Ç–µ–º–∞':<15} {'N':<12} {'œÉ_macro':<12} {'–§–∏–∑–∏—á–µ—Å–∫–∏–π —Å–º—ã—Å–ª'}")

for name, N in sizes.items():
    sigma_macro = physical_sigma_r(r=1000, N_total=N, l_p=l_p)
    print(f"{name:<15} {N:<11.0e} {sigma_macro:<11.2e} {sigma_macro:.0e}")